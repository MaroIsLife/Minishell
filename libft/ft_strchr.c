#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;
	int		a;

	i = 0;
	a = ft_strlen(s);
	while (i <= a)
	{
		if (s[i] == c)
		{
			return ((char *)&s[i]);
		}
		i++;
	}
	return (0);
}
